def addition(num1, num2)
  result = num1 + num2
  return result
end

def subtraction(num1, num2)
  result = num1 - num2
  return result
end

def multiply(num1, num2)
  result = num1 * num2
  return result
end

def division(num1, num2)
  result = num1 / num2
  return result
end

def remainder(num1, num2)
  result = num1 % num2
  return result
end

def exponentiation(num1, num2)
  result = num1 ** num2
  return result
end
